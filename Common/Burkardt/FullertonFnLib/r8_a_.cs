    public static double r8_atan2(ref r8ATAN2Data data, double sn, double cs)

        //****************************************************************************80
        //
        //  Purpose:
        //
        //    R8_ATAN2 evaluates the arc-tangent of two R8 arguments.
        //
        //  Licensing:
        //
        //    This code is distributed under the GNU LGPL license. 
        //
        //  Modified:
        //
        //    11 September 2011
        //
        //  Author:
        //
        //    Original FORTRAN77 version by Wayne Fullerton.
        //    C++ version by John Burkardt.
        //
        //  Reference:
        //
        //    Wayne Fullerton,
        //    Portable Special Function Routines,
        //    in Portability of Numerical Software,
        //    edited by Wayne Cowell,
        //    Lecture Notes in Computer Science, Volume 57,
        //    Springer 1977,
        //    ISBN: 978-3-540-08446-4,
        //    LC: QA297.W65.
        //
        //  Parameters:
        //
        //    Input, double SN, CS, the Y and X coordinates of a 
        //    point on the angle.
        //
        //    Output, double R8_ATAN2, the arc-tangent of the angle.
        //
    {
        double value;

        switch (data.sml)
        {
            case 0.0:
                data.sml = r8_mach(1);
                data.big = r8_mach(2);
                break;
        }

        //
        //  We now make sure SN can be divided by CS.  It is painful.
        //
        double abssn = Math.Abs(sn);
        double abscs = Math.Abs(cs);

        if (abscs <= abssn)
        {
            switch (abscs)
            {
                case < 1.0 when abscs * data.big <= abssn:
                    switch (sn)
                    {
                        case < 0.0:
                            value = -0.5 * Math.PI;
                            break;
                        case 0.0:
                            Console.WriteLine("");
                            Console.WriteLine("R8_ATAN2 - Fatal error!");
                            Console.WriteLine("  Both arguments are 0.");
                            return 1;
                        default:
                            value = 0.5 * Math.PI;
                            break;
                    }

                    return value;
            }
        }
        else
        {
            switch (abscs)
            {
                case > 1.0 when abssn <= abscs * data.sml:
                {
                    value = cs switch
                    {
                        >= 0.0 => 0.0,
                        _ => Math.PI
                    };

                    return value;
                }
            }
        }

        value = Math.Atan(sn / cs);

        switch (cs)
        {
            case < 0.0:
                value += Math.PI;
                break;
        }

        switch (value)
        {
            case > Math.PI:
                value -= 2.0 * Math.PI;
                break;
        }

        return value;
    }

