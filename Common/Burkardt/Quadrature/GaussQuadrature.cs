                Console.WriteLine("  L <= 25 required.");
                Console.WriteLine("  Input L = " + l + "");
                return (1);
            }
            else if (l <= 25)
            {
                n = l;
            }
            else
            {
                Console.WriteLine("");
                Console.WriteLine("GQU_ORDER - Fatal error!");
                Console.WriteLine("  L <= 25 required.");
                Console.WriteLine("  Input L = " + l + "");
                return (1);
            }

            return n;
        }



