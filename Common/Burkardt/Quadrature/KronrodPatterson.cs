        public static int kpn_order(int l)

            //****************************************************************************80
            //
            //  Purpose:
            //
            //    KPN_ORDER computes the order of a KPN rule from the level.
            //
            //  Licensing:
            //
            //    This code is distributed under the GNU LGPL license.
            //
            //  Modified:
            //
            //    08 December 2012
            //
            //  Author:
            //
            //    John Burkardt.
            //
            //  Parameters:
            //
            //    Input, int L, the level of the rule.  
            //    1 <= L <= 25
            //
            //    Output, int KPN_ORDER, the order of the rule.
            //
        {
            int n;

            if (l < 1)
            {
                Console.WriteLine("");
                Console.WriteLine("KPN_ORDER - Fatal error!");
                Console.WriteLine("  1 <= L <= 25 required.");
                Console.WriteLine("  Input L = " + l + "");
                return 1;
            }
            else if (l == 1)
            {
                n = 1;
            }
            else if (l <= 3)
            {
                n = 3;
            }
            else if (l == 4)
            {
                n = 7;
            }
            else if (l <= 8)
            {
                n = 9;
            }
            else if (l == 9)
            {
                n = 17;
            }
            else if (l <= 15)
            {
                n = 19;
            }
            else if (l == 16)
            {
                n = 31;
            }
            else if (l == 17)
            {
                n = 33;
            }
            else if (l <= 25)
            {
                n = 35;
            }
            else
            {
                Console.WriteLine("");
                Console.WriteLine("KPN_ORDER - Fatal error!");
                Console.WriteLine("  1 <= L <= 25 required.");
                Console.WriteLine("  Input L = " + l + "");
                return (1);
            }

            return n;
        }

